#3. Find the average amount spent on coffee, per transaction, for each person
people = [
  {
      name: 'Sarah',
      transactions: [
          {
              type: 'COFFEE',
              amount: 7.43
          },
          {
              type: 'TACOS',
              amount: 14.65
          },
          {
              type: 'COFFEE',
              amount: 4.43
          }
      ]
  },
  {
      name: 'Bandari',
      transactions: [
          {
              type: 'BIKES',
              amount: 800.00
          },
          {
              type: 'TACOS',
              amount: 14.65
          },
          {
              type: 'COFFEE',
              amount: 4.43
          }
      ]
  },
  {
      name: 'Safwan',
      transactions: [
          {
              type: 'COFFEE',
              amount: 7.43
          },
          {
              type: 'COFFEE',
              amount: 100.00
          },
          {
              type: 'COFFEE',
              amount: 4.43
          }
      ]
  }
]


coffee_average_per_person = []

for person in people
    coffee_counter=0
    personal_sum=0
    for transaction in person[:transactions]
        if transaction[:type]=='COFFEE'
            coffee_counter+=1
            personal_sum+=transaction[:amount]
        end
    end
    coffee_average_per_person.push({name: person[:name],average: (personal_sum/coffee_counter)})
end
p coffee_average_per_person